    private void selecionarFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarFotoActionPerformed
        try {
            Selecionar selecionar = new Selecionar(null, 3, null, null);
            if (selecionar.pegarCaminho() != null) {
                if (selecionar.pegarNome().substring(selecionar.pegarNome().lastIndexOf('.') + 1).equals("jpg") || selecionar.pegarNome().substring(selecionar.pegarNome().lastIndexOf('.') + 1).equals("png")) {
                    timagem.setText(selecionar.pegarCaminho());
                    caminho = "resources/fotosProdutos/" + selecionar.pegarNome();
                    BufferedImage image = ImageIO.read(new File("resources\\fotosProdutos\\" + selecionar.pegarNome()));
                    imagemSelecionada.setIcon(new ImageIcon(image.getScaledInstance(mostrarImagem.getWidth(), mostrarImagem.getHeight(), 1)));
                } else {
                    JOptionPane.showMessageDialog(null, "Você deve selecionar uma imagem nos formatos jpg, jpge ou png!", "ERRO!", JOptionPane.ERROR_MESSAGE);
                }
            }
            selecionar.fechar();
        } catch (Exception ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_selecionarFotoActionPerformed

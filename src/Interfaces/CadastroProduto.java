    private void tquantidade2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tquantidade2KeyReleased
        try {
            if (tquantidade2.getText().equals("")) {
                produto.setQuantidade(0);
            } else {
                produto.setQuantidade(Integer.parseInt(tquantidade2.getText()));
            }
            produto.setTotal();
            ttotal.setText(real.format(produto.getTotal()));
        } catch (NumberFormatException ex) {
        }


    }//GEN-LAST:event_tquantidade2KeyReleased


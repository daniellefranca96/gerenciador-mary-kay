    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        TableModelEstoque model = (TableModelEstoque) tabela.getModel();
        lista.removeAll(lista);
        lista = model.getDadosTabela();
        linhas = model.getLinhasEditadas();
        int i = 0;
        int cont = linhas.size();
        while (cont != 0) {
            conexao.setProduto(2, lista.get(linhas.get(i)));
            if (lista.get(linhas.get(i)).getQuantidade() == 0) {
                model.removeProduto(linhas.get(i));
            }
            i++;
            cont--;
        }
        JOptionPane.showMessageDialog(null, "Alterações salvas com suceso!", "AVISO!", JOptionPane.INFORMATION_MESSAGE);
        TableModelEstoque model2 = new TableModelEstoque(model.getDadosTabela());
        tabela.setModel(model2);
    }//GEN-LAST:event_editarActionPerformed

    private void removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerActionPerformed
        TableModelEstoque model = (TableModelEstoque) tabela.getModel();
        Produto produto = model.getProduto(tabela.getSelectedRow());
        model.removeProduto(tabela.getSelectedRow());
        tabela.setModel(model);
        produto.setQuantidade(0);
        produto.setTotal();
        conexao.setProduto(2, produto);
    }//GEN-LAST:event_removerActionPerformed


    public void setProdutos(ArrayList<Produto> lista) {
        lista.removeAll(this.lista);
        this.lista = lista;
        int i = 0;
        int cont = lista.size();
        while (cont != 0) {
            TableModelEstoque model = (TableModelEstoque) tabela.getModel();
            model.addProduto(lista.get(i));
            i++;
            cont--;
        }
    }

    public void listarTodos() {
        try {
            lista.removeAll(lista);
            lista = conexao.getEstoque(1);
            TableModelEstoque modelo = new TableModelEstoque(lista);
            tabela.setModel(modelo);
        } catch (NotFoundItemException ex) {
            JOptionPane.showMessageDialog(null, "Esse nome não está cadastrado no sistema!", "ERRO!", JOptionPane.ERROR_MESSAGE);
        } catch (EmptyBankException ex) {
            JOptionPane.showMessageDialog(null, "Não há dados cadastrados no sistema!", "AVISO!", JOptionPane.INFORMATION_MESSAGE);
        }
    }

